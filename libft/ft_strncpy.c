#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	size_t	i;

	if (s1 && s2)
	{
		i = 0;
		while (n > 0 && s2[i] != '\0')
		{
			s1[i] = s2[i];
			n--;
			i++;
		}
		while (n > 0)
		{
			s1[i] = '\0';
			n--;
			i++;
		}
	}
	return (s1);
}

