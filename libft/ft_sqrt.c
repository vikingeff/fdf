#include "libft.h"

int		ft_sqrt(int n)
{
	int	res;
	int	i;

	res = 0;
	i = 1 << 30;
	while (i > n)
		i >>= 2;
	while (i != 0)
	{
		if (n >= res + i)
		{
			n -= res + i;
			res = (res >> 1) + i;
		}
		else
			res >>= 1;
		i >>= 2;
	}
	return (res);
}
